bool process_record_user(uint16_t keycode, keyrecord_t* record) {
    switch (keycode) {
        case KC_LCTL:
            if (record->event.pressed) {
                isSneaking = true;
            } else {
                isSneaking = false;
            }
            break;
        case KC_SPC:
            if (record->event.pressed) {
                isJumping  = true;
                showedJump = false;
            } else {
                isJumping = false;
            }
            break;
        case KC_BSPC:
            if (record->event.pressed) {
                isBarking  = true;
            } else {
                isBarking = false;
            }
            break;


            /* KEYBOARD PET STATUS END */
    }
    return true;
}
